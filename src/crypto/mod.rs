//! Модуль криптографии
//!
//! Реализует постквантовое шифрование (Kyber), симметричное шифрование (XChaCha20)
//! и ключевой обмен (X25519 Diffie-Hellman).

pub mod dh;
pub mod kyber;
pub mod xchacha;

use anyhow::Result;

/// Инициализация криптографического модуля
pub fn init() -> Result<()> {
    log::info!("Инициализация криптографического модуля...");

    // Проверка доступности криптографических примитивов
    kyber::test()?;
    xchacha::test()?;
    dh::test()?;

    log::info!("Все криптографические примитивы доступны");
    Ok(())
}
